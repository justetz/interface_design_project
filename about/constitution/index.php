            <div class="col-xs-12 constitution">
                <h1>The Rensselaer Union Constitution</h1>

                <h2>Article I<br />Name</h2>
                <p class="lead">The name of this organization shall be the Rensselaer Union, hereinafter referred to as the Union.</p>
                <hr>

                <h2>Article II<br />Membership</h2>
                <p class="lead">All students, both graduate and undergraduate, presently enrolled at Rensselaer Polytechnic Institute, hereinafter referred to as Rensselaer, who have paid the Union Activity Fee, shall constitute the membership of the Union.</p>
                <hr>

                <h2>Article III<br />Purpose</h2>
                <p class="lead">The purpose of this Union shall be to unite all its members in a commitment to the ideals for which Rensselaer stands, to expand the extracurricular life at Rensselaer, to coordinate all student organizations, to act as a medium through which student opinion may be expressed, and to work with all other members of the Rensselaer community to encourage student initiative and to lead student action in all interests which will serve the welfare and the betterment of Rensselaer.</p>
                <hr>

                <h2>Article IV<br />Officers</h2>
                <ol>
                    <li>The officers of the Union shall be the Grand Marshal, the President of the Union, the Chairperson of the Judicial Board, the President of the Graduate Council, and the President of the Undergraduate Council.</li>
                    <li>It shall be the duty of the Union officers to meet regularly to discuss policy and facilitate a closer coordination of their activities.</li>
                </ol>
                <h3>Section 1. Grand Marshal.</h3>
                <p class="lead">The Grand Marshal shall be the presiding officer of the Student Senate and an ex-officio member of all committees.</p>
                <ol>
                    <li>The Grand Marshal shall be regarded as the leader and the chief spokesperson for the entire Union.</li>
                    <li>The Grand Marshal shall be a non-voting member of the Student Senate, but may cast one vote in the event of a tie.</li>
                    <li>Any appointments made by the Grand Marshal may be nullified by a majority vote of the total voting membership of the Student Senate.</li>
                </ol>

                <h3>Section 2. President of the Union.</h3>
                <p class="lead">The President of the Union shall be the presiding officer of the Executive Board, and an ex-officio member of all Union committees.</p>
                <ol>
                    <li>The President of the Union shall be a non-voting member of the Executive Board but may cast one vote in the event of a tie.</li>
                    <li>Any appointments made by the President of the Union other than members of the Executive Board must be ratified by a 2/3 vote of the Executive Board</li>
                </ol>

                <h3>Section 3. Chairperson of the Judicial Board.</h3>
                <p class="lead">The Chairperson of the Judicial Board shall be the presiding officer of the Judicial Board.</p>
                <ol>
                    <li>The Chairperson shall be a non-voting member of the Judicial Board, but in the event of the tie may cast one vote.</li>
                </ol>

                <h3>Section 4. President of the Graduate Council.</h3>
                <p class="lead">The President of the Graduate Council shall be the presiding officer of the Graduate Council and an ex-officio member of all graduate committees.</p>
                <ol>
                    <li>The President of the Graduate Council shall be elected by the Graduate Council from among its members.</li>
                    <li>His or her voting status will be determined via the by-laws of the Graduate Council. </li>
                </ol>

                <h3>Section 5. President of the Undergraduate Council.</h3>
                <p class="lead">The President of the Undergraduate Council shall be the presiding officer of the Undergraduate Council and an ex-officio member of all undergraduate committees.</p>
                <ol>
                <li>The President of the Undergraduate Council shall be a non-voting member of the Undergraduate Council but may cast one vote in the case of a tie.</li>
                </ol>

                <h3>Section 6. Term Limits.</h3>
                <ol>
                    <li>The offices of Grand Marshal and President of the Union are open to any student who has been enrolled in Rensselaer for at least one full semester.</li>
                    <li>A student may hold the position of either Grand Marshal or President of the Union for no more than a combined total of two full terms.</li>
                </ol>
                <hr>

                <h2>Article V: Director Of The Union</h2>
                    <p class="lead">The Director of the Union shall be employed by the Union to perform duties including the administration of Union activities and funds in accordance with the policies of the Rensselaer Board of Trustees, the Union Constitution, and further legislation of the Executive Board.</p>
                    <p class="lead">In maintaining advisory status, the Director may not veto any Executive Board action but may request the Student Senate to begin impeachment proceedings against any member of the Student Senate or the Executive Board whom he or she feels is fiscally irresponsible in the handling of Union funds.</p>
                    <p class="lead">Should the Director overstep his or her constitutional authority or demonstrate serious financial irresponsibility, a majority of the Executive Board may bring charges against the Director in the name of the Board before the Judicial Board.</p>
                <p class="lead">The Vice President for Student Life shall act as adviser to the Director and shall inform him or her of Trustee policy.</p>
                <hr>

                <h2>Article VI: Treasurer Of The Union</h2>
                <p class="lead">The Treasurer shall be the Treasurer of Rensselaer.</p>
                <p class="lead">The Treasurer shall be the custodian of the funds of the Union and shall receive copies of all financial transactions.</p>
                <hr>

                <h2>Article VII: Organizations</h2>
                <p class="lead">The Union shall be governed by a Student Senate, an Executive Board, a Judicial Board, a Graduate Council, and an Undergraduate Council.</p>
                <ol>
                    <li>It shall be constituted under the authority of the Board of Trustees of Rensselaer.</li>
                </ol>

                <h3>Section 1. Student Senate.</h3>
                <p class="lead">The Student Senate shall be the chief legislative and policy-making branch of the Union.</p>
                <ol>
                    <li>The Student Senate shall be presided over by the Grand Marshal.</li>
                    <li>The Student Senate shall consist of 26 voting members: Six Senators from the Graduate Class, four Senators from each undergraduate class, two Independent Senators, and two Greek Senators.</li>
                    <li>It shall be responsible for initiating all legislative action in those areas which affect the Union.</li>
                    <li>It shall pass such by-laws as are necessary for its operation by a 2/3 vote of its total voting membership.</li>
                    <li>It shall determine the amount of the Union Activity Fee by a 2/3 vote of its total voting membership.</li>
                    <li>It shall establish procedures for selection and removal of the student members of the committees of the Board of Trustees and all other appointments.</li>
                    <li>It shall be the responsibility of all senators to regularly report to their respective councils.</li>
                </ol>

                <h3>Section 2. Executive Board.</h3>
                <p class="lead">The Executive Board shall act as the primary budgeting body of the Union.</p>
                <ol>
                    <li>The Executive Board shall be presided over by the President of the Union.</li>
                    <li>The Executive Board shall consist of a minimum of 15 voting members, and a maximum of 20 voting members:
                        <ol type="a">
                            <li>The following positions on the Executive Board must be filled: 5 club or intercollegiate athletics representatives, 1 representative each from the Freshman, Sophomore, Junior, Senior, and Graduate classes, a representative of the Graduate Council, a representative of the Undergraduate Council, a Senate-Executive Board Liaison, and 2 Members-at-Large representing any membership of the Union.</li>
                            <li>The following positions on the Executive Board may be filled at the discretion of the President of the Union: 2 club or intercollegiate athletic members, and 3 Members-at-Large.</li>
                            <li>The Senate-Executive Board Liaison, Graduate Council Representative, and Undergraduate Council Representative are appointed by the Senate, Graduate Council, and Undergraduate Council respectively. All other positions are appointed by the President of the Union and approved by a 2/3 vote of the Senate.</li>
                            <li>If a member of the Executive Board is unable to fulfill his or her duties, a replacement may be appointed by the President of the Union, subject to a majority vote of the Executive Board and a 2/3 approval of the Senate.</li>
                            <li>The Director of the Union and his or her assistants shall act as advisers to the Executive Board.</li>
                        </ol>
                    </li>
                    <li>Such by-laws, as are necessary for its operation, shall be passed by a 2/3 vote of its total voting membership followed by a majority vote of the Student Senate.</li>
                    <li>It shall investigate, prepare, and approve the budget for the following fiscal year. The budget and the records of all additional expenditures and appropriations shall be public documents.</li>
                    <li>It shall administer the Union budget, appropriate Union funds and manage the business affairs of any and all facilities operated by the Rensselaer Union.</li>
                    <li>It shall approve the hiring and continuance of all administrative personnel of the Union.</li>
                    <li>It shall decide on the classification of new organizations and changes in the classification of old ones, as specified in its by-laws.</li>
                </ol>

                <h3>Section 3. Judicial Board.</h3>
                <ol>
                    <li>The Judicial Board shall be the primary court of judgment for cases in which the concept of student rights, responsibilities, or conduct is in question.
                        <ol type="a">
                            <li>No other board shall assume any portion of this jurisdiction, unless such a board be comprised solely of students, and the jurisdiction of the board clearly defined.</li>
                            <li>No such board will be constituted nor its jurisdiction changed without the consent of the Student Senate and Institute President.</li>
                        </ol>
                    </li>
                    <li>The Judicial Board shall be the sole organization responsible for the interpretation of the Union Constitution.</li>
                    <li>The Judicial Board Chairperson shall be the presiding officer and a non-voting member of the Judicial Board.</li>
                    <li>The Judicial Board shall consist of 6 voting members and 5 alternate members, all of whom must be members of the Rensselaer Union.
                        <ol type="a">
                            <li>The chairperson shall appoint new candidates to the Judicial Board subject to a 2/3 approval of the Judicial Board followed by a 2/3 approval of the Student Senate.</li>
                            <li>Alternate members are non-voting members of the Judicial Board who may assume voting rights under circumstances specified in the Judicial Board bylaws.</li>
                            <li>All appointments become effective when a vacancy exists in the position to which they are appointed. Appointments shall become effective in the order of their approval by the Judicial Board.</li>
                            <li>Once appointed, members of the Judicial Board remain active until they are no longer a member of the Rensselaer Union, have been removed from office, or have resigned their position.</li>
                        </ol>
                    </li>
                    <li>Any charge which specifies a violation of the Union Constitution, the Rensselaer Student Bill of Rights, or established regulations governing student conduct may be referred to the Judicial Board. In deciding a case, the Judicial Board may declare any legislation of the Student Senate, the Executive Board, the Graduate Council, the Undergraduate Council, or the Undergraduate Class Councils to be unconstitutional.</li>
                    <li>Such by-laws, as are necessary for its operation, shall be passed by a 2/3 vote of its total voting membership followed by a majority vote of the Student Senate.</li>
                </ol>


                <h3>Section 4. Graduate Council.</h3>
                <ol>
                    <li>The Graduate Council is the graduate legislative branch of the Union. Its voting membership shall consist of the six Graduate Senators and five representatives elected at large. It shall be presided over by the President of the Graduate Council.</li>
                    <li>It shall be responsible for initiating all legislative action within the Union pertaining to the graduate students not specifically dealt with by the Student Senate or the Executive Board.</li>
                    <li>It shall recommend any change in the amount of graduate Class Dues by a majority vote, subject to a 2/3 approval of the Student Senate’s total voting membership.</li>
                    <li>The Graduate Council shall be responsible for the expenditure of graduate Class Dues, collected from the members of its class by majority vote.</li>
                    <li>Such by-laws, as are necessary for its operation, shall be passed by a 2/3 vote of its total voting membership followed by a majority vote of the Student Senate.</li>
                </ol>

                <h3>Section 5. Undergraduate Council.</h3>
                <ol>
                    <li>The Undergraduate Council is the undergraduate legislative branch of the Union. It shall consist of the Class President and Vice President of each Undergraduate Class as voting members, and shall be presided over by the President of the Undergraduate Council.
                        <ol type="a">
                            <li>It shall be responsible for coordination and oversight of the four Undergraduate Class Councils.</li>
                            <li>All expenditures of Class Dues by the undergraduate Class Councils are subject to approval by a majority vote of the Undergraduate Council, in accordance with guidelines set forth in the by-laws of the Undergraduate Council.</li>
                            <li>It shall recommend any change in the amount of undergraduate Class Dues by a majority vote, subject to a 2/3 approval of the Student Senate’s total voting membership.</li>
                            <li>Such by-laws, as are necessary for its operation, shall be passed by a 2/3 vote of its total voting membership followed by a majority approval of the Student Senate.</li>
                        </ol>
                    </li>
                    <li>Each of the Senior, Junior, Sophomore, and Freshman classes shall elect a Class Council. Class Councils exist to promote the interests and unity of their Class.
                        <ol type="a">
                            <li>The Class President is the presiding officer and a non-voting member of the Class Council. The President may vote to break a tie in any vote of the Class Council.</li>
                            <li>The Class Vice President is an officer and voting member of the Class Council.</li>
                            <li>Voting membership in each Class Council shall include a minimum of eight voting Representative positions, their respective Class Senators, and the Vice President. Additional positions may be defined in the by-laws of the Undergraduate Council.</li>
                            <li>The President and Vice President of each Class Council shall serve as voting members on the Undergraduate Council.</li>
                            <li>Each Class Council shall recommend the expenditure of Class Dues, collected from the members of its class, by majority vote, subject to majority approval of the Undergraduate Council.</li>
                            <li>Each Class Council shall pass legislation commensurate with its duties, subject to regulations established in the bylaws of the Undergraduate Council and this Constitution.</li>
                        </ol>
                    </li>
                    <li>No person may hold more than one officer position, as defined by this Constitution or the Undergraduate Council Bylaws, on the same Class Council. The Undergraduate Council President may not hold a voting position nor be an officer of any Class Council.</li>
                </ol>

                <h3>Section 6. Parliamentary Authority.</h3>
                <p class="lead">The rules contained in the current edition of Robert's Rules of Order shall govern the subordinate bodies of the Rensselaer Union, as defined in the Rensselaer Union Constitution, in all cases to which they are applicable and in which they are not inconsistent with the Rensselaer Union Constitution and any special rules of order the Union or its subordinate bodies may adopt.</p>
                <hr>

                <h2>Article VIII: Elections</h2>
                <h3>Section 1.</h3>
                <p class="lead">The general elections of the Grand Marshal, the President of the Union, the President of the Undergraduate Council, the elected members of the Sophomore, Junior and Senior Class Councils, the members of the Graduate Council, and all elected members of the Student Senate except those representing the incoming Freshman Class shall be held during the spring semester at a time and place determined by the Student Senate.</p>

                <h3>Section 2.</h3>
                <ol>
                    <li>The conclusion of an election is defined as the time at which its results are released.</li>
                    <li>The results of an election may be released no later than the end of the semester in which the election is held.</li>
                </ol>

                <h3>Section 3.</h3>
                <ol>
                <li>The elections of the Freshman Class Council and Freshman members of the Student Senate shall be held during the fall semester at a time and place determined by the Student Senate.</li>
                <li>The Officers, Representatives, and Senators for the Freshman Class shall serve for a term beginning on the date of their election and ending with the conclusion of the next general election for their respective position.</li>
                </ol>

                <h3>Section 4.</h3>
                <p class="lead">Voting shall be by secret ballot. Only members of the Union at the time of the elections shall have the right to vote or run for office.</p>

                <h3>Section 5. Greek Status.</h3>
                <ol>
                    <li>To be eligible for the office of Independent Senator, a student must not be a member of a fraternity or sorority at Rensselaer.</li>
                    <li>Fraternal affiliation shall be determined by the Dean of Students Office.</li>
                    <li>To be eligible for the office of Greek Senator, a student must be a member of a fraternity or sorority at Rensselaer. Of the candidates for Greek Senator, the fraternity member with the highest number of votes and the sorority member with the highest number of votes will be elected as Greek Senators.</li>
                </ol>

                <h3>Section 6.</h3>
                <ol>
                    <li>The Student Senate shall determine the means whereby a student may have his or her name placed on the ballot.</li>
                    <li>It shall also be responsible for providing adequate notice of all upcoming elections to the members of the Union.</li>
                    <li>The Student Senate will be responsible for maintaining proportionate representation of all members of the Union on the Student Senate, the Graduate Council, the Undergraduate Council, the Executive Board, and the Judicial Board.</li>
                </ol>

                <h3>Section 7.</h3>
                <p class="lead">All elected offices, unless otherwise specified in this Constitution, shall be for a term beginning upon the date of their election and ending with the conclusion of the next general election for their respective positions.</p>

                <h3>Section 8.</h3>
                <p class="lead">The candidates for Senators, representatives to the Graduate Council, the Undergraduate Class Councils, and President of the Undergraduate Council receiving the highest numbers of votes shall be elected.</p>

                <h3>Section 9.</h3>
                <ol>
                    <li>Vacancies in any elective office may be filled by appointment, subject to the provisions of the constitution or by-laws of the body to which the appointment is made.</li>
                    <li>Such an appointment shall be valid for the remainder of the term of office.</li>
                </ol>

                <h3>Section 10.</h3>
                <ol>
                    <li>Forty-percent of the popular vote shall be necessary for election in the case of Grand Marshal and the President of the Union.</li>
                    <li>A plurality shall be necessary for election in the case of Undergraduate Class Council officers.</li>
                    <li>The number of representatives to each class council will be specified by its constitution.</li>
                </ol>

                <h3>Section 11.</h3>
                <p class="lead">If no victor can be declared in an election, another election will be held between the tied candidates at a time and place determined by the Student Senate in its by-laws.</p>

                <h3>Section 12.</h3>
                <ol>
                    <li>The installation of all newly elected persons shall be within one week following the conclusion of general elections.</li>
                    <li>The President of the Union shall appoint all required members of the Executive Board, excluding the Graduate Council representative, the Undergraduate Council representative, the Freshman class member, and the Senate-Executive Board Liaison, within three weeks after the conclusion of general elections for the President of the Union.
                        <ol type="a">
                            <li>The Undergraduate Council representative shall be appointed by the Undergraduate Council within three weeks following the conclusion of general elections for the Undergraduate Class Councils.</li>
                            <li>The Graduate Council representative shall be appointed by the Graduate Council within three weeks following the conclusion of general elections for the Graduate Council.</li>
                            <li>The Student Senate representative shall be chosen by the Student Senate within three weeks following the conclusion of general elections for the Student Senate.</li>
                            <li>The President of the Union shall appoint the Freshman class member within three weeks after the fall elections.</li>
                            <li>The President of the Union shall appoint any discretionary members by the end of the 4<sup>th</sup> week of the fall semester.</li>
                            <li>Term of office shall normally be one year but a member will continue to serve until he/she has been replaced.</li>
                        </ol>
                    </li>
                    <li>The Graduate Council shall appoint its at-large members at the first regular meeting following the conclusion of general elections for the Graduate Council. They shall be selected by the full eleven member Graduate Council from all applicants. Applications must be solicited for at least two weeks prior to all selection meetings.</p>
                </ol>

                <h3>Section 13.</h3>
                <p class="lead">To be eligible for election and to hold office, a student must satisfy the eligibility rules established by the Institute and the additional regulations established by the Student Senate.</p>
                <hr>

                <h2>Article IX: Limitation Of Office</h2>
                <h3>Section 1.</h3>
                <p class="lead">A candidate for Grand Marshal or President of the Union may simultaneously run for a Senator or Representative position in his or her Class Council or the Graduate Council.</p>
                <ol>
                    <li>He or she may not run for an officer position on that Council, nor may he or she hold more than one office should he or she be elected to both.</li>
                </ol>

                <h3>Section 2.</h3>
                <p class="lead">The Grand Marshal shall not be a member of the Executive Board, nor any committee thereof, nor shall the President of the Union be a member of the Student Senate, nor shall either be a member of a constituent council of the Student Senate, nor of any Union judiciary body.</p>

                <h3>Section 3.</h3>
                <p class="lead">No person may simultaneously run for more than one position on the same council.</p>

                <h3>Section 4.</h3>
                <p class="lead">No person may run simultaneously for the offices of Grand Marshal and President of the Union. </p>
                <hr>

                <h2>Article X: Removals</h2>
                <h3>Section 1.</h3>
                <ol>
                    <li>The Grand Marshal, the President of the Union, or the President of the Undergraduate Council may be removed from office by a petition requiring his or her resignation signed by at least 50 percent of his or her constituents or by a 2/3 vote of his or her constituents voting in a valid removal election.</li>
                    <li>A valid removal election is one in which at least 20 percent of his or her constituents vote.</li>
                    <li>A removal election may be called by a 2/3 vote of the Student Senate or by a petition signed by at least 20 percent of his or her constituents followed by a majority vote of the Student Senate.</li>
                </ol>

                <h3>Section 2.</h3>
                <ol>
                    <li>The Student Senate may remove a voting member for good cause, as specified in the Student Senate by-laws, by a 2/3 vote of its total voting membership.</li>
                    <li>Elected senators may be recalled by their constituencies. A 2/3 vote of his or her constituents in a valid removal election would constitute a recall. Such a removal election may be brought about by a petition signed by 20 percent of his or her constituents or by a majority vote of the Student Senate. Senators filling seats normally appointed by a council, may be removed by a 2/3 vote of the council.</li>
                    <li>Should a vacancy remain unfilled by the body which made the appointment that body shall forfeit the vote of the unoccupied position.</li>
                </ol>

                <h3>Section 3.</h3>
                <ol>
                    <li>Any members of the Executive Board whom have been installed by the Student Senate may be removed by a 2/3 vote of the Student Senate, or by the simple request of the President of the Union supported by a majority vote of the Student Senate.</li>
                    <li>The members of the Executive Board appointed by the Graduate Council, Undergraduate Council, or the Student Senate may be removed by a 2/3 vote of the appointing body, or by the simple request of the President of the Union supported by a majority vote of the appointing body.</li>
                </ol>

                <h3>Section 4.</h3>
                <p class="lead">A student member of the Judicial Board or Review Board may be recalled for good cause by 2/3 vote of the Student Senate.</p>

                <h3>Section 5.</h3>
                <ol>
                    <li>Any member of the Graduate Council may be removed from office for good cause as specified in their by-laws, with a 2/3 vote of the Graduate Council. Any member of the Undergraduate Class Councils may be removed from office for good cause as specified in the Undergraduate Council by-laws, by a 2/3 of their total voting membership of their Class Council.</li>
                    <li>The person whom the action concerns shall not have the right to vote in the removal decision. An impeachment vote, requiring majority approval, may be called from the floor at any Council meeting. If the impeachment vote is successful, a removal vote will be held at the following meeting.</p>
                </ol>
                <hr>

                <h2>Article XI: Succession</h2>

                <h3>Section 1.</h3>
                <p class="lead">Should the position of Grand Marshal become vacant, within two weeks the Student Senate shall appoint a new Grand Marshal from within its membership. His or her appointment shall require the approval of 2/3 vote of the Student Senate. The Chairperson of the Judicial Board will preside over this appointment. Until the new Grand Marshal is appointed, the Student Senate by-laws shall dictate who will assume the remaining duties of the Grand Marshal.</p>

                <h3>Section 2.</h3>
                <p class="lead">Should the position of President of the Union become vacant, within two weeks the Executive Board shall appoint from within its membership a new President. His or her appointment shall be confirmed by a 2/3 vote of the Student Senate. The Executive Board by-laws shall dictate who will preside over the Executive Board until the new President of the Union is confirmed.</p>

                <h3>Section 3.</h3>
                <p class="lead">Should the position of Judicial Board Chairman become vacant, within two weeks the Judicial Board shall nominate a new Judicial Board Chairman from among the membership of the Judicial Board. The nominee shall be confirmed by a two-thirds vote of the Senate. Until a new Chairman has been confirmed, the Judicial Board by-laws shall dictate who will assume the duties of the Chairman.</p>

                <h3>Section 4.</h3>
                <p class="lead">Should the position of the President of the Undergraduate Council become vacant, all active Class Councils shall meet in joint session within two weeks, for the purpose of electing a new President of the Undergraduate Council. His or her appointment shall require a 2/3 approval of the Student Senate. The Grand Marshal shall preside over this appointment. Until the new President of the Undergraduate Council is appointed, the Undergraduate Council By-laws shall dictate who will assume the remaining duties of the President.</p>

                <h3>Section 5.</h3>
                <p class="lead">If any elective position including by not limited to, Officers of the Union, is filled by appointment, a special election for that position shall be held if the Student Senate receives, within two weeks of the announcement of the appointment, a petition signed by 20 percent of the constituency involved. If the appointment is made at a time when the full student body is not normally on campus, the petition must be received within two weeks of the announcement to the full campus.</p>

                <h3>Section 6.</h3>
                <p class="lead">Any vacant Class Senator position shall be filled by a person appointed by their corresponding Class Council.</p>
                <hr>

                <h2>Article XII: Petitions And Referenda</h2>
                <h3>Section 1. Referendum.</p>
                <ol>
                    <li>A referendum vote, on any matter except amendment of the Union Constitution, shall be held whenever the Student Senate:
                        <ol type="a">
                            <li>Approves any form of referendum question proposed by a Senator, Committee, Council, or petition, by a 2/3 vote.</li>
                            <li>Receives a petition signed by at least 10% of all members of the Union, and does not reject it. Such a petition may only be rejected by a 2/3 vote of the total voting membership of the Senate.</p>
                        </ol>
                    </li>
                </ol>

                <h3>Section 2.</h3>
                <p class="lead">The referendum election shall be held at a time determined by the Student Senate, not more than fortyfive days (exclusive of vacation periods) or at the next regular election, whichever comes first, but in no case less than fourteen days following its approval. A referendum may be held at any time during the academic year except during exam periods.</p>

                <h3>Section 3.</h3>
                <p class="lead">A referendum shall become effective if approved by a majority vote of students in a valid referendum election. A valid referendum election is one in which at least 20 percent of the affected constituency vote.</p>

                <h3>Section 4.</h3>
                <p class="lead">Upon its passage, a referendum shall be binding upon the constituency. The Student Senate shall enact any legislation necessary for its implementation. Unless otherwise provided for in the text of the referendum, it may be rescinded only by a similar referendum.</p>
                <hr>

                <h2>Article XIII: Amendments</h2>

                <h3>Section 1.</h3>
                <ol>
                    <li>Any amendment of the Union Constitution must be passed by a 2/3 vote of the Student Senate followed by a majority vote of the students voting in a valid constitutional election.</li>
                    <li>A valid constitutional election shall be one in which 20 percent of those eligible vote.</li>
                </ol>

                <h3>Section 2.</h3>
                <ol>
                    <li>One month prior to a general student body vote on the amendment, it must be submitted to the President of the Institute or his/her chosen representative who may refer the amendment to the Board of Trustees for their consideration.</li>
                </ol>

                <h3>Section 3.</h3>
                <ol>
                    <li>At least two weeks prior to the general student body vote on the amendment, the exact wording of the amendment to be voted on shall be made available to the student body through a campus-wide publication.</li>
                </ol>

                <h3>Section 4.</h3>
                <ol>
                    <li>An amendment vote may be held at any time during the school year excluding exam periods.</li>
                </ol>

                <h3>Section 5.</h3>
                <ol>
                    <li>A record of all amendments shall be appended to the Rensselaer Union Constitution for posterity.</li>
                </ol>
                <hr>

                <h2>Article XIV: Liability</h2>
                <p class="lead">No liability shall be incurred by this Union through its governing bodies, officers, or members unless provision has been made for it. No liability shall be incurred or money spent by any committee, officer, or member of the Union without a requisition from the Executive Board or its duly authorized agent.</p>
