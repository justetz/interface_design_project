                <h2>About the Grand Marshal</h2>
                <p class="lead">The Grand Marshal (GM) is the oldest and highest elected office on campus and the presiding officer of the Student Senate. The GM is a non-voting member of the Senate, and plays an important role in Student Government projects, appointment of officers, and the greater campus community.  They support the efforts of the Senate with student groups, the surrounding community, and key administrators including the President of the Institute’s Cabinet.</p>
                <p class="lead">The position of Grand Marshal was created in 1866 to honor Major Albert Metcalf Harper, a Delta Phi brother, civil war veteran, and a well respected and admired student. Albert’s role was to lead and represent the Rensselaer student body.  Every year following, with the exception of a few years in the 1890s, the RPI student body has elected a Grand Marshal to serve as their student body president. Grand Marshall Week and associated celebrations formed around the election for this prestigious position. The GM wears an iconic top-hat as a symbol of their position.</p>
